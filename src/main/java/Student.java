public class Student {

    private int id;
    private int grade;
    private int class_num;
    private int num;
    private String name;

    public Student(int id, int grade, int class_num, int num, String name) {
        this.id = id;
        this.grade = grade;
        this.class_num = class_num;
        this.num = num;
        this.name = name;
    }




}
